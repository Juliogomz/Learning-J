public class Flutuante {
    public static void main(String[] args) throws Exception {
        float altura;
        altura = 1.70f;
        System.out.println(altura);
        //No tipo de variável float é possível armazenar valores com casas decimais.
        //OBS: Quando for utilizar float é preciso identificar usando um “f” após o número decimal.

        double PI;
        PI = 3.1415926535;
        System.out.println(PI);
        //O tipo de variável double corresponde a basicamente a mesma funcionalidade do float.
        //A única diferença é que ela pode armazenar muito mais valores que o float é capaz de armazenar.

    }
}
